public class Coordinates {
    // :) :(
    final private int posX;
    final private int posY;

    public Coordinates(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }


}
